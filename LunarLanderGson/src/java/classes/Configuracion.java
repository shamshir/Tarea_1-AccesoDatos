package classes;

public class Configuracion {
    
    private String nombre;
    private String modoDificil;
    private String colorNave;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getModoDificil() {
        return modoDificil;
    }

    public void setModoDificil(String modoDificil) {
        this.modoDificil = modoDificil;
    }

    public String getColorNave() {
        return colorNave;
    }

    public void setColorNave(String colorNave) {
        this.colorNave = colorNave;
    }
    
}
